t=0

game = {
 state = 0 -- attract mode
}

player = {
 x = 7*24,y =3*24,
 aim = -1
}

monster = {
	x = 1*24,y = 3*24,
	rot = 0
}

function TIC()
 t=t+2
 if game.state == 0 then attractMode() end	 
end

function attractMode()
	cls(0)
	updatePlayer()
	updateMonster()
	drawMap()
	drawPlayer()
	drawMonster()
	drawText()
end

function updatePlayer()
 if btn(0) then 
  player.aim = -1 end
 if btn(1) then 
  player.aim = -1 end
 if btn(2) then 
  player.aim = -1 end
 if btn(3) then 
  player.aim = -1 end

 if btn(4) then player.aim = 270 end
 if btn(5) then player.aim = 180 end
 if btn(6) then player.aim = 0 end
 if btn(7) then player.aim = 90 end
end

function updateMonster()
 if monster.x < 1*24 then
	 monster.rot = 0
	end
	if monster.x > 5*24 then
	 monster.rot = 1
	end
	if monster.rot == 0 then
	 monster.x = monster.x + 1
	end
	if monster.rot == 1 then
		monster.x = monster.x - 1 
	end
end	

function drawMap() -- draw 64x64 Sprite Map
	for i=0,9 do
	 for j=0,5 do
		-- Get the mini-sprite value from TIC-80 map
		val=peek(0x08000+i+j*240) 
		drawMapSprite(val,i,j)
	 end
 end
end

function drawMapSprite(val,i,j)
 -- Wall 1 
 if val == 240 then spr(1,i*24,j*24,-1,1,0,0,3,3) end
 -- Secret Wall 1
 if val == 241 then
	 spr(1,i*24,j*24,-1,1,0,0,3,3)
	 spr(16,i*24+8,j*24+8,-1,1,0,0,1,1)
 end
 -- Wall 2 
 if val == 242 then spr(4,i*24,j*24,-1,1,0,0,3,3) end
 -- Stairs left
 if val == 243 then 
	 spr(7,i*24,j*24,-1,1,0,0,2,3) 
  spr(8,i*24+16,j*24,-1,1,0,0,1,3) 
 end
 -- Stairs center
 if val == 244 then
	 spr(8,i*24,j*24,-1,1,0,0,1,3)
	 spr(8,i*24+8,j*24,-1,1,0,0,1,3)
	 spr(8,i*24+16,j*24,-1,1,0,0,1,3) 
 end
 -- Stairs right
 if val == 245 then 
	 spr(8,i*24,j*24,-1,1,0,0,1,3) 
  spr(8,i*24+8,j*24,-1,1,0,0,2,3) 
 end
 -- Pikes
 if val == 246 then spr(96,i*24,j*24,-1,1,0,0,3,3) end
 -- Little Skull
 if val == 247 then spr(140,i*24,j*24,-1,1,0,0,3,2) end
 -- Big Skull
 if val == 248 then spr(172,i*24,j*24,-1,1,0,0,3,2) end
 -- Aztec Statue
 if val == 249 then spr(99,i*24,j*24,-1,1,0,0,3,3) end
 -- Weird Statue
 if val == 250 then spr(54,i*24,j*24,-1,1,0,0,3,3) end
 -- Pedestal
 if val == 251 then spr(102,i*24,j*24,-1,1,0,0,3,3) end
 -- Book
 if val == 252 then spr(252,i*24+8,j*24+16,-1,1,0,0,1,1) end
end

function drawPlayer()
 if player.aim == -1 then spr(256,player.x,player.y,0,1,0,0,3,3) end
 if player.aim == 0 then spr(304,player.x,player.y,0,1,0,0,3,3) end
 if player.aim == 90 then spr(310,player.x,player.y,0,1,0,0,3,3) end
 if player.aim == 180 then spr(307,player.x,player.y,0,1,0,0,3,3) end
 if player.aim == 270 then spr(313,player.x,player.y,0,1,0,0,3,3) end
end

function drawMonster()
	spr(352+t%60//30*3,monster.x,monster.y,0,1,monster.rot,0,3,3)
end

function drawText()
	print("Axayacalt's",65,1*24+6,10,2,2)
	print("Tomb 2",90,2*24,10,2,2)
	print("A TIC-80 game by gist974",50,5*24+2,1,1,1)
	print("Graphics: Petitjean & Shurder",35,5*24+10,1,1,1)
end
